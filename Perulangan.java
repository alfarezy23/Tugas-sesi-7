public class Perulangan {
    public static void main(String[] args) {

        System.out.println("=== Loop Sederhana ===");
        int[] angka = {6, 2, 7, 1, 8, 0, 9, -1, 10};
        for (int i = 0; i < angka.length; i++) {
            System.out.print(angka[i] + " ");
        }
        System.out.println("\n");
    }
}